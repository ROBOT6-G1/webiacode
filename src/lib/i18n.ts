export function getLanguage(): Language {
  const saved = typeof window !== "undefined" ? localStorage.getItem("devwebia_language") : null;
  if (saved === "fr" || saved === "mg" || saved === "en" || saved === "zh" || saved === "it") {
    return saved;
  }
  return "fr";
}

export function setGlobalLanguage(lang: Language) {
  if (typeof window !== "undefined") {
    localStorage.setItem("devwebia_language", lang);
    window.dispatchEvent(new Event("devwebia_language_changed"));
  }
}

export function useI18n() {
  const [lang, setLangState] = useState<Language>(getLanguage);

  useEffect(() => {
    const handleStorage = () => {
      setLangState(getLanguage());
    };
    window.addEventListener("storage", handleStorage);
    window.addEventListener("devwebia_language_changed", handleStorage);
    return () => {
      window.removeEventListener("storage", handleStorage);
      window.removeEventListener("devwebia_language_changed", handleStorage);
    };
  }, []);

  const changeLanguage = (newLang: Language) => {
    setLangState(newLang);
    setGlobalLanguage(newLang);
  };

  const t = translations[lang] || translations.fr;

  return { lang, changeLanguage, t };
}
